# Condicionales anidadas - programa que convierte nº a letras.
número = int(input("Di un número que quieras convertir"))
if número == 1: 
    print("El número es igual a uno")
# si quiero seguir dándole isntrucciones a la misma variable, se usa "elif".
elif número ==3: 
    print("El número es igual a dos")
elif número ==3: 
    print ("El número es igual a tres")
else: 
    print ("Este programa solo convierte el 1, 2 y 3.")
